    private class OyenteVentana implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {
            
            String texto = campoLados.getText();
            try{
                panelCentro.setLados(Integer.parseInt(texto));
            } catch(NumberFormatException ex){
                
                JOptionPane.showMessageDialog(panelCentro, "Captura un valor numerico entero", "Error de captura", JOptionPane.ERROR_MESSAGE);
                panelCentro.setLados(0);
                campoLados.setText("");
                
            }
            panelCentro.repaint();   
        }
    }
    
